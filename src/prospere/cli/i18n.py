_current_lang: dict[str, str] = MENU_I18N["en"]
_current_lang_code: str = "en"


def set_language(code: str) -> None:
    """Set the active language for all translation lookups."""
    global _current_lang, _current_lang_code
    code = resolve_lang(code)
    _current_lang_code = code
    _current_lang = MENU_I18N.get(code, MENU_I18N["en"])


def get_language() -> str:
    """Return the current language code."""
    return _current_lang_code


def t(key: str, *args: Any, **kwargs: Any) -> str:
    """Look up a translated string, optionally formatting with args/kwargs."""
    text = _current_lang.get(key, MENU_I18N["en"].get(key, key))
    if args or kwargs:
        text = text.format(*args, **kwargs)
    return text


