def detect_lang() -> str:
    """Detect system language. Returns 'en', 'zh-Hant', or 'zh-Hans'."""
    for key in ("LANG", "LC_ALL", "LC_CTYPE"):
        val = os.environ.get(key, "").lower()
        if not val:
            continue
        if any(tag in val for tag in ("zh-cn", "zh-hans", "zh_sg", "zh-sg")):
            return "zh-Hans"
        if any(tag in val for tag in ("zh-tw", "zh-hk", "zh-hant", "zh-mo")):
            return "zh-Hant"
        if val.startswith("zh"):
            return "zh-Hant"
    return "en"


def resolve_lang(code: str) -> str:
    """Resolve a language code to a canonical key. Backward compat for 'zh'."""
    if code in ("zh", "zh-Hant"):
        return "zh-Hant"
    if code in ("zh-Hans", "zh-CN"):
        return "zh-Hans"
    return code


